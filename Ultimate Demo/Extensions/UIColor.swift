import UIKit

extension UIColor {
    
    static var customDarkBlue: UIColor {
        return UIColor(named: "CustomDarkBlue") ?? .white
    }
    
    static var customGray: UIColor {
        return UIColor(named: "CustomGray") ?? .white
    }
    
    static var customGreen: UIColor {
        return UIColor(named: "CustomGreen") ?? .white
    }
    
    static var customRed: UIColor {
        return UIColor(named: "CustomRed") ?? .white
    }
    
    static var customDarkGray: UIColor {
        return UIColor(named: "CustomDarkGray") ?? .white
    }
    
}
